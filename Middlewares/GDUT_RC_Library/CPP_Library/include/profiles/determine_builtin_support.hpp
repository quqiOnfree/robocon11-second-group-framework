                                      // VS2022 intellisense issue.
#if !defined(GDUT_USING_BUILTIN_IS_ASSIGNABLE)
#define GDUT_USING_BUILTIN_IS_ASSIGNABLE __has_builtin(__is_assignable)
#endif

